-- View: live weighted-average rate per (mill, count) — used by costing engine
CREATE VIEW v_yarn_weighted_avg AS
SELECT
  yl.mill_id,
  yl.yarn_count_id,
  SUM(yl.current_kg) AS available_kg,
  SUM(yl.current_kg * yl.cost_per_kg) /
    NULLIF(SUM(yl.current_kg), 0) AS weighted_avg_cost
FROM yarn_lot yl
WHERE yl.current_kg > 0
GROUP BY yl.mill_id, yl.yarn_count_id;

