CREATE TABLE costing_master (
  id                      bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  quality_code            text NOT NULL UNIQUE,     -- 120HT
  quality_name            text NOT NULL,
  fabric_type             fabric_type NOT NULL,
  production_mode         production_mode NOT NULL,
  status                  record_status NOT NULL DEFAULT 'active',

  -- Warp inputs
  warp_count_id           bigint NOT NULL REFERENCES yarn_count(id),
  warp_ends               integer,                  -- nullable; auto-calc fallback
  tape_length_m           numeric(10,4) NOT NULL,
  shrinkage_pct           numeric(7,4) NOT NULL DEFAULT 0,
  yarn_wastage_pct        numeric(7,4) NOT NULL DEFAULT 0.02,    -- 2%

  -- Weft inputs
  weft_count_id           bigint NOT NULL REFERENCES yarn_count(id),
  pick_ppi                numeric(7,4) NOT NULL,    -- picks per inch
  fabric_length_m         numeric(10,4) NOT NULL,
  weft_allowance_m        numeric(7,4) NOT NULL DEFAULT 2,

  -- Reed / width / selvedge
  reed_count              integer NOT NULL,
  fabric_width_in         numeric(7,4) NOT NULL,
  selvedge_ends           integer NOT NULL DEFAULT 0,

  -- Bobbin block (optional; up to 2)
  use_bobbin_1            boolean NOT NULL DEFAULT false,
  bobbin_1_id             bigint REFERENCES bobbin(id),
  bobbin_1_loading        numeric(10,4) DEFAULT 0.10,
  use_bobbin_2            boolean NOT NULL DEFAULT false,    -- lurex
  bobbin_2_id             bigint REFERENCES bobbin(id),
  bobbin_2_loading        numeric(10,4) DEFAULT 0.10,

  -- Porvai block (towels only)
  use_porvai              boolean NOT NULL DEFAULT false,
  porvai_count_id         bigint REFERENCES yarn_count(id),
  porvai_slevage_length_m numeric(7,4),
  porvai_wastage_pct      numeric(7,4) NOT NULL DEFAULT 0.02,

  -- Cost rate inputs (manual overrides; nullable means use live purchase rate)
  pick_paise_market       numeric(8,4),             -- for Quoted Cost
  vendor_pick_paise       numeric(8,4),             -- for True Cost when outsourced
  sizing_cost_per_m       numeric(10,4) NOT NULL DEFAULT 0,
  auto_cost_per_m         numeric(10,4) NOT NULL DEFAULT 0,
  warp_commission_per_m   numeric(10,4) NOT NULL DEFAULT 0,
  fabric_commission_per_m numeric(10,4) NOT NULL DEFAULT 0,

  -- Provenance & approval
  save_path               costing_save_path NOT NULL DEFAULT 'formal',
  approval_status         approval_status NOT NULL DEFAULT 'pending',
  approved_by             uuid REFERENCES app_user(id),
  approved_at             timestamptz,
  source_so_id            bigint,                   -- if quick_quote, link to SO

  notes                   text,
  created_at              timestamptz NOT NULL DEFAULT NOW(),
  created_by              uuid REFERENCES app_user(id),
  updated_at              timestamptz NOT NULL DEFAULT NOW(),
  updated_by              uuid REFERENCES app_user(id),

  CONSTRAINT chk_porvai_only_for_towel CHECK (
    use_porvai = false OR fabric_type = 'towel'
  ),
  CONSTRAINT chk_porvai_has_count CHECK (
    use_porvai = false OR porvai_count_id IS NOT NULL
  ),
  CONSTRAINT chk_bobbin1_when_used CHECK (
    use_bobbin_1 = false OR bobbin_1_id IS NOT NULL
  ),
  CONSTRAINT chk_bobbin2_when_used CHECK (
    use_bobbin_2 = false OR bobbin_2_id IS NOT NULL
  )
