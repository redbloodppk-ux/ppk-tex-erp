-- Bobbin stock (3-location per Porvai+Bobbin Spec v2.1 §1.3)
CREATE TABLE bobbin_stock (
  id              bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  bobbin_id       bigint NOT NULL REFERENCES bobbin(id),
  location        bobbin_location NOT NULL,
  vendor_id       bigint REFERENCES mill(id),         -- if location = at_vendor
  customer_id     bigint REFERENCES customer(id),     -- if location = customer_owned
  quantity_pcs    numeric(10,4) NOT NULL DEFAULT 0,   -- decimal: partial pieces tracked
  updated_at      timestamptz NOT NULL DEFAULT NOW(),
  CONSTRAINT chk_location_party CHECK (
    (location = 'main_godown' AND vendor_id IS NULL AND customer_id IS NULL) OR
    (location = 'at_vendor' AND vendor_id IS NOT NULL AND customer_id IS NULL) OR
    (location = 'customer_owned' AND vendor_id IS NULL AND customer_id IS NOT NULL)
  )
);
CREATE UNIQUE INDEX uq_bobbin_stock_loc ON bobbin_stock(
  bobbin_id, location,
  COALESCE(vendor_id, 0), COALESCE(customer_id, 0)
);

