-- Function to atomically allocate next document number
CREATE OR REPLACE FUNCTION fn_next_doc_no(p_doc_type text)
RETURNS text AS $$
DECLARE
  v_row doc_sequence%ROWTYPE;
  v_seq text;
BEGIN
  UPDATE doc_sequence
    SET next_value = next_value + 1, updated_at = NOW()
    WHERE doc_type = p_doc_type
    RETURNING * INTO v_row;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Unknown doc_type %', p_doc_type;
  END IF;
  -- Format: PREFIX-FY-SEQ padded to 4
  v_seq := LPAD((v_row.next_value - 1)::text, 4, '0');
  RETURN v_row.prefix || '-' || v_row.fy_code || '-' || v_seq;
END;
$$ LANGUAGE plpgsql;

