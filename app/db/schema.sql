-- Days-of-cover per yarn count (reorder forecasting — Master Spec v3.0 §2.7)
CREATE OR REPLACE VIEW v_yarn_days_of_cover AS
WITH consumption_30d AS (
  SELECT
    pb.warp_lot_id AS lot_id,
    SUM(pb.produced_m * COALESCE(c.warp_cost_per_m / NULLIF(yl.cost_per_kg,0), 0)) AS kg_consumed
  FROM production_batch pb
  JOIN yarn_lot yl ON yl.id = pb.warp_lot_id
  LEFT JOIN v_costing_computed c ON c.id = pb.costing_id
  WHERE pb.start_date >= CURRENT_DATE - 30
  GROUP BY pb.warp_lot_id
)
SELECT
  yc.id AS yarn_count_id,
  yc.code,
  yc.display_name,
  COALESCE(SUM(yl.current_kg), 0) AS available_kg,
  COALESCE(SUM(c30.kg_consumed), 0) AS kg_30d,
  CASE WHEN COALESCE(SUM(c30.kg_consumed), 0) > 0
    THEN (COALESCE(SUM(yl.current_kg), 0) / (COALESCE(SUM(c30.kg_consumed), 0) / 30))::numeric(10,2)
    ELSE NULL END AS days_of_cover
FROM yarn_count yc
LEFT JOIN yarn_lot yl ON yl.yarn_count_id = yc.id AND yl.current_kg > 0
LEFT JOIN consumption_30d c30 ON c30.lot_id = yl.id
GROUP BY yc.id, yc.code, yc.display_name;

