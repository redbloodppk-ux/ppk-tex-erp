-- 7. Master: Yarn Count (Module 17) — supports cotton + polyester (Porvai)
-- -----------------------------------------------------------------------------
CREATE TABLE yarn_count (
  id              bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code            text NOT NULL UNIQUE,           -- C-30, C-150D, etc.
  display_name    text NOT NULL,                  -- "30s combed cotton", "150D Porvai"
  yarn_type       yarn_type NOT NULL DEFAULT 'cotton',

  -- Cotton: user enters Ne directly
  ne              numeric(8,4),                   -- English count

  -- Polyester (Porvai): user enters Denier; NeC auto-computed = 5315 / Denier
  denier          numeric(8,4),

  -- Computed once at insert/update so app code never has to recompute
  -- For polyester: nec_computed = 5315 / denier
  -- For cotton:    nec_computed = ne
  nec_computed    numeric(8,4) GENERATED ALWAYS AS (
    CASE
      WHEN yarn_type = 'polyester' AND denier IS NOT NULL AND denier > 0
        THEN 5315 / denier
      ELSE ne
    END
  ) STORED,

  -- Reference data
  tex             numeric(8,4),                   -- 590.5 / Ne (informational)
  is_doubled      boolean NOT NULL DEFAULT false, -- 2/40s, 2/20s
  is_slub         boolean NOT NULL DEFAULT false, -- 20s slub
  reorder_kg      numeric(12,2) NOT NULL DEFAULT 0,
  notes           text,
  status          record_status NOT NULL DEFAULT 'active',
  created_at      timestamptz NOT NULL DEFAULT NOW(),
  created_by      uuid REFERENCES app_user(id),
  updated_at      timestamptz NOT NULL DEFAULT NOW(),
  updated_by      uuid REFERENCES app_user(id),

  -- If polyester, denier required and ne must be null
  CONSTRAINT chk_polyester_has_denier CHECK (
    (yarn_type = 'polyester' AND denier IS NOT NULL AND ne IS NULL)
    OR
    (yarn_type IN ('cotton','blend') AND ne IS NOT NULL)
  )
);
CREATE INDEX idx_yarn_count_type ON yarn_count(yarn_type);

CREATE TRIGGER trg_yarn_count_updated_at BEFORE UPDATE ON yarn_count
  FOR EACH ROW EXECUTE FUNCTION fn_set_updated_at();

