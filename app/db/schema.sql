-- =============================================================================
-- PPK TEX ERP — Database Schema v1.0
-- Target: PostgreSQL 15+ on Supabase (Mumbai region)
-- Spec source: PPK_TEX_ERP_Master_Spec_v3.0, FabricCosting_FrozenSpec_v1.1,
--              Porvai_Bobbin_ERP_Integration_Spec_v2.1, Module11_Attendance_Addendum_v1.0
--
-- Conventions:
--   * All money columns: NUMERIC(14,4)  -- 4 decimals = paise precision
--   * All percentages: NUMERIC(7,4)     -- 0.0200 = 2%
--   * All weights (kg): NUMERIC(12,4)
--   * All lengths (m):  NUMERIC(12,4)
--   * All counts (Ne):  NUMERIC(8,4)
--   * Timestamps: timestamptz, stored UTC, displayed in Asia/Kolkata
--   * Primary keys: bigint generated by default as identity
--   * Soft delete: status enum 'active' | 'archived' | 'discontinued'
--   * Audit columns: created_at, created_by, updated_at, updated_by on every table
-- =============================================================================

