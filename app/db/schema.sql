-- Costing computed view: every quality with both Quoted Cost and True Cost
-- Implements Sections 3-5 of FabricCosting_FrozenSpec_v1.1
CREATE OR REPLACE VIEW v_costing_computed AS
WITH overhead AS (SELECT total_per_m FROM v_looms_overhead),
warp_rate AS (
  SELECT cm.id AS costing_id, COALESCE(wa.weighted_avg_cost, 0) AS rate
  FROM costing_master cm
  LEFT JOIN v_yarn_weighted_avg wa ON wa.yarn_count_id = cm.warp_count_id
),
weft_rate AS (
  SELECT cm.id AS costing_id, COALESCE(wa.weighted_avg_cost, 0) AS rate
  FROM costing_master cm
  LEFT JOIN v_yarn_weighted_avg wa ON wa.yarn_count_id = cm.weft_count_id
),
porvai_rate AS (
  SELECT cm.id AS costing_id, COALESCE(wa.weighted_avg_cost, 0) AS rate
  FROM costing_master cm
  LEFT JOIN v_yarn_weighted_avg wa ON wa.yarn_count_id = cm.porvai_count_id
)
SELECT
  cm.id, cm.quality_code, cm.quality_name, cm.fabric_type, cm.production_mode,
  cm.use_porvai, cm.use_bobbin_1, cm.use_bobbin_2,
  -- Yarn Ne values
  wc.nec_computed AS warp_ne,
  ftc.nec_computed AS weft_ne,
  pc.nec_computed AS porvai_ne,
  -- Live yarn rates
  wr.rate AS warp_rate_per_kg,
  ftr.rate AS weft_rate_per_kg,
  pr.rate AS porvai_rate_per_kg,
  -- Metre per gram (Block 1 — woven fabric formulas)
  -- Warp: 1848 / (Ne × Reed × Width × (1 + Shrinkage%))
  CASE WHEN cm.fabric_type IN ('woven','dupatta') AND wc.nec_computed > 0 AND cm.reed_count > 0 AND cm.fabric_width_in > 0
    THEN 1848.0 / (wc.nec_computed * cm.reed_count * cm.fabric_width_in * (1 + cm.shrinkage_pct))
    ELSE NULL END AS warp_metres_per_gram,
  -- Weft: 1690 / (Ne × Pick × Width)  (towel uses fabric length)
  CASE WHEN ftc.nec_computed > 0 AND cm.pick_ppi > 0 AND cm.fabric_width_in > 0
    THEN 1690.0 / (ftc.nec_computed * cm.pick_ppi * cm.fabric_width_in)
    ELSE NULL END AS weft_metres_per_gram,
  -- Porvai weft per metre (Section 2.6): (1690 × NeC / Pick) / (SlevageLength + 3)
  CASE WHEN cm.use_porvai AND pc.nec_computed > 0 AND cm.pick_ppi > 0 AND cm.porvai_slevage_length_m IS NOT NULL
    THEN (1690.0 * pc.nec_computed / cm.pick_ppi) / (cm.porvai_slevage_length_m + 3)
    ELSE 0 END AS porvai_metres_per_gram,
  -- Per-metre yarn costs (₹/m) = (Rate × (1 + Wastage%)) / MetresPerGram / 1000
  -- (1000 because metres-per-gram → metres-per-kg = ×1000)
  CASE WHEN ftc.nec_computed IS NOT NULL THEN
    (wr.rate * (1 + cm.yarn_wastage_pct)) /
      NULLIF((1848.0 / (wc.nec_computed * cm.reed_count * cm.fabric_width_in * (1 + cm.shrinkage_pct))) * 1000, 0)
    ELSE 0 END AS warp_cost_per_m,
  CASE WHEN ftc.nec_computed IS NOT NULL THEN
    (ftr.rate * (1 + cm.yarn_wastage_pct)) /
      NULLIF((1690.0 / (ftc.nec_computed * cm.pick_ppi * cm.fabric_width_in)) * 1000, 0)
    ELSE 0 END AS weft_cost_per_m,
  CASE WHEN cm.use_porvai AND pc.nec_computed IS NOT NULL THEN
    (pr.rate * (1 + cm.porvai_wastage_pct)) /
      NULLIF(((1690.0 * pc.nec_computed / cm.pick_ppi) / (cm.porvai_slevage_length_m + 3)) * 1000, 0)
    ELSE 0 END AS porvai_cost_per_m,
  -- Bobbin cost per metre (Section 2.5): (BobbinPrice / BobbinMetre) + Loading
  CASE WHEN cm.use_bobbin_1 AND b1.bobbin_metre > 0 THEN
    (b1.bobbin_price / b1.bobbin_metre) + COALESCE(cm.bobbin_1_loading, 0)
    ELSE 0 END AS bobbin_1_cost_per_m,
  CASE WHEN cm.use_bobbin_2 AND b2.bobbin_metre > 0 THEN
    (b2.bobbin_price / b2.bobbin_metre) + COALESCE(cm.bobbin_2_loading, 0)
    ELSE 0 END AS bobbin_2_cost_per_m,
  -- Pick cost (paise → ₹/m): pick_paise × pick_ppi × width / 100
  CASE WHEN cm.pick_paise_market IS NOT NULL THEN
    (cm.pick_paise_market * cm.pick_ppi * cm.fabric_width_in) / 100
    ELSE 0 END AS pick_cost_quoted_per_m,
  CASE WHEN cm.production_mode = 'inhouse' THEN
    (SELECT total_per_m FROM v_looms_overhead)
  WHEN cm.vendor_pick_paise IS NOT NULL THEN
    (cm.vendor_pick_paise * cm.pick_ppi * cm.fabric_width_in) / 100
  ELSE 0 END AS pick_or_overhead_true_per_m,
  -- Other commissions
  cm.sizing_cost_per_m, cm.auto_cost_per_m,
  cm.warp_commission_per_m, cm.fabric_commission_per_m
FROM costing_master cm
LEFT JOIN yarn_count wc  ON wc.id = cm.warp_count_id
LEFT JOIN yarn_count ftc ON ftc.id = cm.weft_count_id
LEFT JOIN yarn_count pc  ON pc.id = cm.porvai_count_id
LEFT JOIN bobbin b1      ON b1.id = cm.bobbin_1_id
LEFT JOIN bobbin b2      ON b2.id = cm.bobbin_2_id
LEFT JOIN warp_rate wr   ON wr.costing_id = cm.id
LEFT JOIN weft_rate ftr  ON ftr.costing_id = cm.id
LEFT JOIN porvai_rate pr ON pr.costing_id = cm.id;

-- Final two-cost view used by every quotation / report
CREATE OR REPLACE VIEW v_costing_two_cost AS
SELECT
  c.*,
  (warp_cost_per_m + weft_cost_per_m + porvai_cost_per_m
   + bobbin_1_cost_per_m + bobbin_2_cost_per_m
   + pick_cost_quoted_per_m
   + sizing_cost_per_m + auto_cost_per_m
   + warp_commission_per_m + fabric_commission_per_m)::numeric(10,4) AS quoted_cost_per_m,
  (warp_cost_per_m + weft_cost_per_m + porvai_cost_per_m
   + bobbin_1_cost_per_m + bobbin_2_cost_per_m
   + pick_or_overhead_true_per_m
   + sizing_cost_per_m + auto_cost_per_m
   + warp_commission_per_m + fabric_commission_per_m)::numeric(10,4) AS true_cost_per_m
FROM v_costing_computed c;

