-- ============================================================================
-- AUTO-CODE TRIGGERS  (SZ-YYYY-NNNN / PV-YYYY-NNNNN)
-- ============================================================================
INSERT INTO doc_sequence (prefix, year, next_no, padding)
VALUES ('SZ', EXTRACT(YEAR FROM CURRENT_DATE)::int, 1, 4),
       ('PV', EXTRACT(YEAR FROM CURRENT_DATE)::int, 1, 5)
ON CONFLICT DO NOTHING;

CREATE OR REPLACE FUNCTION fn_sizing_autogen_code() RETURNS trigger AS $$
DECLARE n int; y int;
BEGIN
  IF NEW.job_code IS NULL OR NEW.job_code = '' THEN
    y := EXTRACT(YEAR FROM CURRENT_DATE)::int;
    UPDATE doc_sequence
       SET next_no = next_no + 1
     WHERE prefix = 'SZ' AND year = y
     RETURNING next_no - 1 INTO n;
    IF n IS NULL THEN
      INSERT INTO doc_sequence (prefix, year, next_no, padding)
        VALUES ('SZ', y, 2, 4)
        RETURNING 1 INTO n;
    END IF;
    NEW.job_code := 'SZ-' || y || '-' || LPAD(n::text, 4, '0');
  END IF;
  RETURN NEW;
END $$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_sizing_autogen_code ON sizing_job;
CREATE TRIGGER trg_sizing_autogen_code BEFORE INSERT ON sizing_job
  FOR EACH ROW EXECUTE FUNCTION fn_sizing_autogen_code();

