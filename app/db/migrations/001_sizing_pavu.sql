-- ============================================================================
-- HOUSEKEEPING — auto-update pavu.status when its assignment changes.
-- ============================================================================
CREATE OR REPLACE FUNCTION fn_pa_sync_pavu_status() RETURNS trigger AS $$
BEGIN
  IF NEW.status IN ('queued','mounted','running') THEN
    UPDATE pavu SET status = 'on_loom'  WHERE id = NEW.pavu_id;
  ELSIF NEW.status IN ('completed','removed') THEN
    UPDATE pavu SET status = 'finished' WHERE id = NEW.pavu_id;
  END IF;
  RETURN NEW;
END $$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_pa_sync_pavu_status ON pavu_assign;
CREATE TRIGGER trg_pa_sync_pavu_status
  AFTER INSERT OR UPDATE OF status ON pavu_assign
  FOR EACH ROW EXECUTE FUNCTION fn_pa_sync_pavu_status();

